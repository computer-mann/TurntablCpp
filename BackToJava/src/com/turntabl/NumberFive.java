package com.turntabl;

public class NumberFive {

    public void tableMultiplication(){
        System.out.println("Multiplication Table.");
        System.out.println();
        for(int i=1;i<=10;i++){
            for(int k=1;k<=12;k++){
                System.out.print(i*k + "  ") ;
            }
            System.out.println();
        }
    }
}
